import UIKit

extension UIColor {
    
    class var deepBlue: UIColor {
        return UIColor(red: 20.0/255.0, green: 24.0/255.0, blue: 27.0/255.0, alpha: 1.0)
    }
    
    class var principalBlue: UIColor {
        return UIColor(red: 9/255, green: 74/255, blue: 153/255, alpha: 1)
    }
}

